  function starRadius(star) {
    const spectral = star.spectral_type || '';
    const luminosityBoost = spectral.includes('Ia') || spectral.includes('Ib') ? 1.22 :
      spectral.includes('II') ? 1.11 :
      spectral.includes('III') ? 1.04 : 1.0;
    const apparentBrightness = 0.205 - 0.019 * (star.vmag || 0);
    return THREE.MathUtils.clamp(apparentBrightness * luminosityBoost, 0.060, 0.34);
  }

  function clusterMemberRadius(star) {
    const spectral = star.spectral_type || '';
    const hotBoost = spectral.startsWith('O') || spectral.startsWith('B') ? 1.22 :
      spectral.startsWith('A') ? 1.10 : 1.0;
    const apparentBrightness = 0.118 - 0.0085 * (star.vmag || 5.0);
    return THREE.MathUtils.clamp(apparentBrightness * hotBoost, 0.020, 0.105);
  }

  function clusterFaintMemberRadius(luminosityRank) {
    // Tiny symbolic stars for the unresolved cluster population.
    // The rank term gives a few brighter anchors without returning to the old boloncio look.
    return THREE.MathUtils.clamp(0.010 + 0.035 * Math.pow(1 - luminosityRank, 2.6), 0.007, 0.040);
  }

