  function makeSeededRandom(seedText) {
    let seed = 2166136261;
    for (let i = 0; i < seedText.length; i++) {
      seed ^= seedText.charCodeAt(i);
      seed = Math.imul(seed, 16777619);
    }
    return () => {
      seed += 0x6D2B79F5;
      let t = seed;
      t = Math.imul(t ^ (t >>> 15), t | 1);
      t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
      return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
    };
  }

  function randomClusterPoint(rng, radii, clusterName) {
    // Visual, Gaia-inspired morphology: dense core + sparse halo + weak tidal stretching.
    // It is intentionally not a clean ellipsoid, so clusters read as stellar systems rather than volumes.
    const theta = rng() * Math.PI * 2;
    const u = rng() * 2 - 1;
    const circle = Math.sqrt(1 - u * u);

    const isCore = rng() < 0.68;
    const shell = isCore ? Math.pow(rng(), 1.8) * 0.42 : 0.34 + Math.pow(rng(), 0.42) * 0.78;
    const tailSign = rng() < 0.5 ? -1 : 1;
    const tail = (!isCore && rng() < 0.42) ? tailSign * Math.pow(rng(), 0.35) : 0;

    let x = radii[0] * shell * circle * Math.cos(theta);
    let y = radii[2] * shell * u;
    let z = radii[1] * shell * circle * Math.sin(theta);

    // Cluster-specific sky orientation so every object is not a clone.
    const angle = ((clusterName.length * 37) % 360) * Math.PI / 180;
    const ca = Math.cos(angle);
    const sa = Math.sin(angle);
    const xr = x * ca - z * sa;
    const zr = x * sa + z * ca;
    x = xr + tail * radii[0] * 0.70;
    z = zr + tail * radii[1] * 0.30;

    return new THREE.Vector3(x, y, z);
  }

  function clusterLabelOffsetVector(pos, radii) {
    const fromSun = pos.clone();
    if (fromSun.lengthSq() < 1) fromSun.set(1, 0.35, 0.45);
    fromSun.normalize();
    // Keep the label just outside the stellar population. Older values were
    // tuned for ellipsoids and pushed extended clusters such as Hyades too far
    // away from their visible member distribution.
    const maxRadius = Math.max(radii[0], radii[1], radii[2]);
    const extra = maxRadius * 0.95 + 3.0;
    return fromSun.multiplyScalar(extra).add(new THREE.Vector3(0, Math.max(1.2, radii[2] * 0.22), 0));
  }

  function addClusterSystem(cluster) {
    const pos = galToVector(cluster);
    const color = ageColor(cluster.age_myr);
    const radii = cluster.radius_pc || [8, 6, 5];

    // v0.9: no ellipsoid/core boloncios. The cluster is represented by a
    // structured stellar population plus an invisible click target.
    const clickRadius = Math.max(radii[0], radii[1], radii[2]) * 1.05;
    const clickTarget = new THREE.Mesh(
      new THREE.SphereGeometry(clickRadius, 18, 12),
      new THREE.MeshBasicMaterial({ color, transparent: true, opacity: 0.0, depthWrite: false })
    );
    clickTarget.position.copy(pos);
    clickTarget.userData = {
      ...cluster,
      layer: 'clusters',
      type: 'open cluster',
      isClusterFocus: true,
      focusRadius: clickRadius,
      description: cluster.description || 'Open cluster represented by a schematic 3D distribution of bright and faint member stars.'
    };
    groups.clusters.add(clickTarget);
    // The invisible cluster object is deliberately NOT added to `selectable`.
    // Clusters are centred only when their label is clicked, not when the
    // mouse passes through their large stellar population on the map.

    const labelAnchor = new THREE.Object3D();
    labelAnchor.position.copy(pos).add(clusterLabelOffsetVector(pos, radii));
    groups.clusters.add(labelAnchor);
    addLabel(labelAnchor, cluster.name, 'clusters', {
      className: 'cluster',
      labelOffset: { x: 0, y: 0 },
      maxDistance: 1050,
      selectObject: clickTarget
    });

    const rng = makeSeededRandom(cluster.name);
    const n = cluster.star_count || 120;
    for (let i = 0; i < n; i++) {
      const p = randomClusterPoint(rng, radii, cluster.name);
      const rank = i / Math.max(1, n - 1);
      const radius = clusterFaintMemberRadius(rank) * (0.58 + rng() * 0.62);
      const hotFraction = cluster.age_myr < 180 ? 0.32 : 0.08;
      const fakeSpectral = rng() < hotFraction ? { spectral_type: rng() < 0.45 ? 'B' : 'A', vmag: 3.5 + rng() * 3.0 } :
        { spectral_type: rng() < 0.65 ? 'G' : 'K', vmag: 5.0 + rng() * 3.0 };
      const mat = starMaterial(fakeSpectral);
      mat.transparent = true;
      mat.opacity = THREE.MathUtils.clamp(0.24 + (1 - rank) * 0.42, 0.20, 0.68);
      const m = new THREE.Mesh(new THREE.SphereGeometry(radius, 7, 7), mat);
      m.position.copy(pos).add(p);
      const glow = addStarGlow(m.position, starColor(fakeSpectral), radius, groups.clusterMembers, { opacity: 0.026, scaleFactor: 6.2, minScale: 0.12, maxScale: 0.46 });
      m.raycast = () => {};
      groups.clusterMembers.add(m);
      registerConstantScreenObject(m, { referenceDistance: 135, minFactor: 0.26, maxFactor: 9.0 });
      if (glow) registerConstantScreenObject(glow, { referenceDistance: 135, minFactor: 0.26, maxFactor: 9.0, isSprite: true });
    }

    (cluster.members || []).forEach((member, idx) => {
      const offset = member.offset_pc || [0, 0, 0];
      const p = new THREE.Vector3(offset[0], offset[2], offset[1]);
      const radius = clusterMemberRadius(member) * 1.05;
      const mesh = new THREE.Mesh(new THREE.SphereGeometry(radius, 12, 12), starMaterial(member));
      mesh.position.copy(pos).add(p);
      const glow = addStarGlow(mesh.position, starColor(member), radius, groups.clusterMembers, { opacity: 0.058, scaleFactor: 7.2, minScale: 0.26, maxScale: 1.05 });
      registerConstantScreenObject(mesh, { referenceDistance: 135, minFactor: 0.26, maxFactor: 9.0 });
      if (glow) registerConstantScreenObject(glow, { referenceDistance: 135, minFactor: 0.26, maxFactor: 9.0, isSprite: true });
      addObject(mesh, {
        ...member,
        parent_cluster: cluster.name,
        age_myr: cluster.age_myr,
        distance_pc: cluster.distance_pc,
        description: member.position_note || ((member.source === 'SIMBAD' || String(member.source || '').includes('manual_seed') || String(member.source || '').includes('manual_curated_seed'))
          ? 'Named bright member using catalogue-style sky coordinates projected at the cluster distance. This gives a realistic projected morphology; full 3D depth will come from Gaia member tables.'
          : 'Representative named bright member. Spectral type and magnitude are approximate catalogue-style values; the 3D position is a visual offset within the schematic cluster, not a Gaia-validated member coordinate.')
      }, 'clusterMembers', member.name || '', {
        type: 'cluster bright member',
        className: 'cluster-member',
        maxDistance: 760,
        onlyWhenSelected: true,
        labelOffset: { x: 10 + (idx % 2) * 4, y: -9 - (idx % 3) * 3 }
      });
    });
  }

