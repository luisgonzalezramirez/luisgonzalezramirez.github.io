  function isSameStarApprox(a, b) {
    const keysA = starIdentityKeys(a);
    const keysB = starIdentityKeys(b);
    for (const key of keysA) {
      if (keysB.has(key)) return true;
    }
    if ([a, b].every((s) => Number.isFinite(s?.X_pc) && Number.isFinite(s?.Y_pc) && Number.isFinite(s?.Z_pc))) {
      const dx = a.X_pc - b.X_pc;
      const dy = a.Y_pc - b.Y_pc;
      const dz = a.Z_pc - b.Z_pc;
      const dv = Math.abs(Number(a.vmag ?? 99) - Number(b.vmag ?? 99));
      if (Math.sqrt(dx * dx + dy * dy + dz * dz) < 0.6 && dv < 1.0) return true;
    }
    return false;
  }

