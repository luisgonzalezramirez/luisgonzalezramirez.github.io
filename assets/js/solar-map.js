  function galToVector(obj) {
    // Prefer precomputed Galactic Cartesian coordinates when the JSON has been
    // generated by scripts/generate_solar_json.py. This avoids small browser-side
    // rounding differences and lets real Gaia/SIMBAD-derived member positions be
    // passed through exactly. Fallback to l,b,d for hand-written objects.
    if (Number.isFinite(obj.X_pc) && Number.isFinite(obj.Y_pc) && Number.isFinite(obj.Z_pc)) {
      return new THREE.Vector3(obj.X_pc * pcScale, obj.Z_pc * pcScale, -obj.Y_pc * pcScale);
    }
    const l = THREE.MathUtils.degToRad(obj.l_deg || 0);
    const b = THREE.MathUtils.degToRad(obj.b_deg || 0);
    const d = (obj.distance_pc || 0) * pcScale;
    const X = d * Math.cos(b) * Math.cos(l);
    const Y = d * Math.cos(b) * Math.sin(l);
    const Z = d * Math.sin(b);
    return new THREE.Vector3(X, Z, -Y);
  }

