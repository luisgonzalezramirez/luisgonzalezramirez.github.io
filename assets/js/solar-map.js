  function gaussianRandom() {
    let u = 0; let v = 0;
    while (u === 0) u = Math.random();
    while (v === 0) v = Math.random();
    return Math.sqrt(-2.0 * Math.log(u)) * Math.cos(2.0 * Math.PI * v);
  }

  function randomPointInSoftEllipsoid(rx, ry, rz) {
    let x = 0; let y = 0; let z = 0; let ok = false;
    for (let t = 0; t < 12 && !ok; t += 1) {
      x = gaussianRandom() * 0.38 * rx;
      y = gaussianRandom() * 0.38 * ry;
      z = gaussianRandom() * 0.38 * rz;
      ok = ((x * x) / (rx * rx) + (y * y) / (ry * ry) + (z * z) / (rz * rz)) <= 1.35;
    }
    return [x, y, z];
  }



  function buildSupportsFromPointRows(pointRows, opts = {}) {
    const sigma = opts.sigma ?? 8;
    return (pointRows || []).map((row) => ({
      position: galToVector(row),
      sigma: Number(row.sigma_pc ?? sigma),
      weight: Number(row.weight ?? row.alpha ?? 1)
    }));
  }

  function buildSupportsFromBlobs(blobs) {
    return (blobs || []).map((blob) => {
      const pos = galToVector(blob);
      const rr = Array.isArray(blob.radius_pc) ? blob.radius_pc : [blob.radius_pc || 8, blob.radius_pc || 8, blob.radius_pc || 6];
      const sigma = Math.max(4, 0.45 * ((Number(rr[0] || 8) + Number(rr[1] || 8) + Number(rr[2] || 6)) / 3));
      return { position: pos, sigma, weight: Math.max(0.8, Number(blob.alpha ?? 1) * 20) };
    });
  }

  function addImplicitCloudSurface(targetGroup, supports, color, opts = {}) {
    if (!Array.isArray(supports) || !supports.length) return null;
    const valid = supports.filter((s) => s && s.position && Number.isFinite(s.position.x) && Number.isFinite(s.position.y) && Number.isFinite(s.position.z));
    if (!valid.length) return null;

    const center = new THREE.Vector3();
    let wsum = 0;
    valid.forEach((s) => {
      const w = Math.max(0.1, Number(s.weight ?? 1));
      center.addScaledVector(s.position, w);
      wsum += w;
    });
    center.multiplyScalar(1 / Math.max(1e-6, wsum));

    const locals = valid.map((s) => ({
      position: s.position.clone().sub(center),
      sigma: Math.max(2.5, Number(s.sigma ?? 8)),
      weight: Math.max(0.08, Number(s.weight ?? 1))
    }));

    let supportExtent = 6;
    locals.forEach((s) => {
      supportExtent = Math.max(supportExtent, s.position.length() + 3.8 * s.sigma);
    });
    const detail = opts.detail ?? (supportExtent > 80 ? 2 : 3);
    const baseRadius = Math.max(opts.minRadius ?? 5, Math.min(opts.maxRadius ?? 320, supportExtent));
    const geometry = new THREE.IcosahedronGeometry(baseRadius, detail);
    const pos = geometry.attributes.position;
    const dir = new THREE.Vector3();

    function fieldAt(p) {
      let acc = 0;
      for (let i = 0; i < locals.length; i += 1) {
        const s = locals[i];
        const d2 = p.distanceToSquared(s.position);
        const sig2 = s.sigma * s.sigma;
        acc += s.weight * Math.exp(-0.5 * d2 / sig2);
      }
      return acc;
    }

    const threshold = opts.threshold ?? Math.max(0.46, Math.min(1.25, 0.24 * Math.sqrt(locals.length)));
    const maxAllowedRadius = opts.maxRadius ?? 760;
    const maxSearch = Math.max(baseRadius * (opts.searchFactor ?? 3.8), maxAllowedRadius * 0.82);
    const minSearch = opts.innerRadius ?? 0.5;

    function radialEnvelope(direction) {
      let best = opts.minRadius ?? 5;
      locals.forEach((s) => {
        const proj = s.position.dot(direction);
        const perp2 = Math.max(0, s.position.lengthSq() - proj * proj);
        const sig2 = s.sigma * s.sigma;
        const nearRay = Math.exp(-0.5 * perp2 / Math.max(1e-6, 5.5 * sig2));
        best = Math.max(best, proj + s.sigma * (1.15 + 2.65 * nearRay));
      });
      return THREE.MathUtils.clamp(best, opts.minRadius ?? 4, maxAllowedRadius);
    }

    function radiusAlong(direction) {
      const samples = opts.raySamples ?? 48;
      let prevR = minSearch;
      let prevVal = fieldAt(direction.clone().multiplyScalar(prevR));
      let wasInside = prevVal >= threshold;
      let lastExit = null;
      let entered = wasInside;

      for (let k = 1; k <= samples; k += 1) {
        const r = minSearch + (maxSearch - minSearch) * (k / samples);
        const val = fieldAt(direction.clone().multiplyScalar(r));
        const inside = val >= threshold;
        if (!wasInside && inside) {
          entered = true;
        }
        if (wasInside && !inside) {
          const frac = THREE.MathUtils.clamp((threshold - prevVal) / Math.max(1e-9, val - prevVal), 0, 1);
          lastExit = prevR + frac * (r - prevR);
        }
        wasInside = inside;
        prevR = r;
        prevVal = val;
      }

      if (lastExit !== null) return THREE.MathUtils.clamp(lastExit, opts.minRadius ?? 4, maxAllowedRadius);
      if (entered) return radialEnvelope(direction);
      return radialEnvelope(direction);
    }

    for (let i = 0; i < pos.count; i += 1) {
      dir.fromBufferAttribute(pos, i);
      dir.normalize();
      const r = radiusAlong(dir);
      pos.setXYZ(i, dir.x * r, dir.y * r, dir.z * r);
    }
    pos.needsUpdate = true;
    geometry.computeVertexNormals();

    const baseColor = new THREE.Color(color);
    const material = new THREE.MeshBasicMaterial({
      color: baseColor.clone().lerp(new THREE.Color(0xffffff), opts.brighten ?? 0.10),
      transparent: true,
      opacity: opts.opacity ?? 0.16,
      depthWrite: false,
      side: THREE.DoubleSide,
      blending: THREE.NormalBlending
    });
    const mesh = new THREE.Mesh(geometry, material);
    mesh.position.copy(center);
    mesh.raycast = () => {};
    targetGroup.add(mesh);

    if (opts.outerShell === true) {
      const shell = new THREE.Mesh(geometry.clone(), new THREE.MeshPhongMaterial({
        color: baseColor.clone().lerp(new THREE.Color(0xffffff), 0.22),
        emissive: baseColor.clone().multiplyScalar(0.08),
        transparent: true,
        opacity: Math.max(0.028, (opts.opacity ?? 0.16) * 0.28),
        shininess: 6,
        depthWrite: false,
        side: THREE.BackSide
      }));
      shell.position.copy(center);
      shell.scale.multiplyScalar(1.06);
      shell.raycast = () => {};
      targetGroup.add(shell);
    }
    return mesh;
  }

