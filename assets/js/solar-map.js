  function cssHexToThreeColor(value, fallback) {
    if (!value || typeof value !== 'string') return fallback;
    const clean = value.trim().replace('#', '0x');
    const parsed = Number(clean);
    return Number.isFinite(parsed) ? parsed : fallback;
  }

