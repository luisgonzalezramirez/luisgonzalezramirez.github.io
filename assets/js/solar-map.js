  function renderCloudMesh(targetGroup, meshData, fallbackColor, opts = {}) {
    const verts = meshData.vertices || [];
    const faces = meshData.faces || [];
    if (!verts.length || !faces.length) return null;
    const color = meshData.color ? cssHexToThreeColor(meshData.color, fallbackColor) : fallbackColor;
    const positions = [];
    verts.forEach((v) => {
      const p = galToVector({ X_pc: Number(v[0]), Y_pc: Number(v[1]), Z_pc: Number(v[2]) });
      positions.push(p.x, p.y, p.z);
    });
    const indices = [];
    faces.forEach((face) => {
      if (face.length >= 3) indices.push(Number(face[0]), Number(face[1]), Number(face[2]));
    });
    if (!positions.length || !indices.length) return null;
    const geo = new THREE.BufferGeometry();
    geo.setAttribute('position', new THREE.Float32BufferAttribute(positions, 3));
    geo.setIndex(indices);
    geo.computeVertexNormals();
    const base = new THREE.Color(color);
    const mat = new THREE.MeshPhongMaterial({
      color: base.clone().lerp(new THREE.Color(0xffffff), opts.brighten ?? 0.08),
      emissive: base.clone().multiplyScalar(opts.emissive ?? 0.10),
      transparent: true,
      opacity: opts.opacity ?? meshData.alpha ?? 0.24,
      shininess: 14,
      depthWrite: false,
      side: THREE.DoubleSide
    });
    const mesh = new THREE.Mesh(geo, mat);
    mesh.raycast = () => {};
    targetGroup.add(mesh);
    if (opts.edge === true) {
      const wire = new THREE.Mesh(geo.clone(), new THREE.MeshBasicMaterial({
        color,
        transparent: true,
        opacity: Math.max(0.018, (opts.opacity ?? meshData.alpha ?? 0.24) * 0.13),
        wireframe: true,
        depthWrite: false
      }));
      wire.raycast = () => {};
      targetGroup.add(wire);
    }
    return mesh;
  }

  function renderSurfacePolygon(targetGroup, surface, fallbackColor) {
    const pts = surface.points || [];
    if (pts.length < 3) return null;
    const color = surface.color ? cssHexToThreeColor(surface.color, fallbackColor) : fallbackColor;
    const zPc = Number(surface.Z_pc || 0);
    const thickness = Number(surface.thickness_pc || 7);
    const alpha = Number(surface.alpha ?? 0.075);
    const cx = pts.reduce((s, p) => s + Number(p[0]), 0) / pts.length;
    const cy = pts.reduce((s, p) => s + Number(p[1]), 0) / pts.length;
    const shapePts = pts.map((p) => new THREE.Vector2(Number(p[0]) - cx, Number(p[1]) - cy));
    const triangles = THREE.ShapeUtils.triangulateShape(shapePts, []);
    const vertices = [];
    shapePts.forEach((p) => vertices.push(p.x, 0, p.y));
    const indices = [];
    triangles.forEach((tri) => indices.push(tri[0], tri[1], tri[2]));
    const geo = new THREE.BufferGeometry();
    geo.setAttribute('position', new THREE.Float32BufferAttribute(vertices, 3));
    geo.setIndex(indices);
    geo.computeVertexNormals();

    const group = new THREE.Group();
    group.position.set(cx, zPc, cy);
    const makeMat = (opacity, brighten = 0.0) => new THREE.MeshBasicMaterial({
      color: new THREE.Color(color).lerp(new THREE.Color(0xffffff), brighten),
      transparent: true,
      opacity,
      depthWrite: false,
      side: THREE.DoubleSide,
      blending: THREE.NormalBlending
    });
    const mid = new THREE.Mesh(geo, makeMat(alpha, 0.10));
    mid.raycast = () => {};
    group.add(mid);
    const top = new THREE.Mesh(geo, makeMat(alpha * 0.32, 0.22));
    top.position.y = thickness * 0.50;
    top.raycast = () => {};
    group.add(top);
    const bot = new THREE.Mesh(geo, makeMat(alpha * 0.24, 0.02));
    bot.position.y = -thickness * 0.50;
    bot.raycast = () => {};
    group.add(bot);

    const closed = [...shapePts, shapePts[0]];
    const linePts = closed.map((p) => new THREE.Vector3(p.x, thickness * 0.53, p.y));
    const edge = new THREE.Line(
      new THREE.BufferGeometry().setFromPoints(linePts),
      new THREE.LineBasicMaterial({ color, transparent: true, opacity: Math.min(0.24, alpha * 1.8), depthWrite: false })
    );
    edge.raycast = () => {};
    group.add(edge);
    targetGroup.add(group);
    return group;
  }

